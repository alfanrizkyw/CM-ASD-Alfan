package CM;

public class Pesanan {
    int kodePesanan;
    String namaPesanan;
    int harga;

    // Konstruktor Pesanan
    public Pesanan(int kodePesanan, String namaPesanan, int harga) {
        this.kodePesanan = kodePesanan;
        this.namaPesanan = namaPesanan;
        this.harga = harga;
    }
}
